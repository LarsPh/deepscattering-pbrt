void CloudMie::createCerp() {
    alglib::spline1dbuildcubic(Theta, PDF, CerpPDF);
    alglib::spline1dbuildcubic(Theta, RPhase, CerpR);
    alglib::spline1dbuildcubic(Theta, GPhase, CerpG);
    alglib::spline1dbuildcubic(Theta, BPhase, CerpB);
    alglib::spline1dbuildcubic(CDF, Theta, CerpInv);    
}



Float CloudMie::Sample_p(const Vector3f &wo, Vector3f *wi,
                         const Point2f &u) const {
    ProfilePhase _(Prof::PhaseFuncSampling);
    // Compute theta for Mie sample
    Float theta, cosTheta, sinTheta, phi;
    // Float y1, y2, x1, x2, t;

    // +180.0: The conversion of outgoing wi in pbrt
    theta = 180.0 - alglib::spline1dcalc(CerpInv, u[0]);

    cosTheta = std::cos(ToRad(theta));
    // Compute direction _wi_ for Henyey--Greenstein sample
    sinTheta = std::sin(ToRad(theta));
    phi = 2 * Pi * u[1];
    Vector3f v1, v2;
    CoordinateSystem(wo, &v1, &v2);
    *wi = SphericalDirection(sinTheta, cosTheta, phi, v1, v2, -wo);
    return phaseMie(180.0 - theta);
}

Float CloudMie::p(const Vector3f &wo, const Vector3f &wi, Spectrum &f) const {
    ProfilePhase _(Prof::PhaseFuncEvaluation);
    // -wi: considering the different convention between pbrt and literature on
    // the direction of wi
    Float degree = ToDegrees(BetterAcos(Dot(wo, -wi)));
    return phaseMie(degree, f);
}

